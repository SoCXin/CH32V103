#include "zf_systick.h"
#include "core_riscv.h"
#include "rtthread.h"
//-------------------------------------------------------------------------------------------------------------------
//  @brief      嘀嗒定时器延时
//  @param      time                us
//  @return     void
//  Sample usage:                   内部使用，用户无需关心
//-------------------------------------------------------------------------------------------------------------------
void systick_delay(uint16 time)
{
    uint8 i;
    do{
        i = 5;
        while(--i);
    }while(--time);
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      嘀嗒定时器延时
//  @param      time                ms
//  @return     void
//  Sample usage:                   内部使用，用户无需关心
//-------------------------------------------------------------------------------------------------------------------
void systick_delay2(uint64 time)
{
    rt_thread_mdelay(time);
}


//-------------------------------------------------------------------------------------------------------------------
//  @brief      获得当前System tick timer的值
//  @return     返回当前System tick timer的值
//  Sample usage:               uint32 tim = systick_getval();
//-------------------------------------------------------------------------------------------------------------------
uint32 systick_getval(void)
{
    return (*(volatile uint32*)0xE000F004);
}


